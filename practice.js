"use strict";
// pre increament //
let a = 5;
let preIncreament = ++a;
console.log("pre increament value:", preIncreament);
console.log("value after pre increament:", a);
// post increament //
let postIncreament = a++;
console.log("post increament vale:", postIncreament);
console.log("value after post increament:", a);
// pre decreament //
let b = 9;
let preDecreament = --b;
console.log("pre decreament value:", preDecreament);
console.log("value after pre decreament", b);
// post decreament //
let postDecreament = b--;
console.log("post decreament value:", postDecreament);
console.log("value after post decreament:", b);
